package com.mizhousoft.redis.lettuce.command;

/**
 * User
 *
 * @version
 */
public class TestUser
{
	private int id;

	private String name;

	/**
	 * 获取id
	 * 
	 * @return
	 */
	public int getId()
	{
		return id;
	}

	/**
	 * 设置id
	 * 
	 * @param id
	 */
	public void setId(int id)
	{
		this.id = id;
	}

	/**
	 * 获取name
	 * 
	 * @return
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * 设置name
	 * 
	 * @param name
	 */
	public void setName(String name)
	{
		this.name = name;
	}

	/**
	 * 
	 * @return
	 */
	@Override
	public String toString()
	{
		StringBuilder builder = new StringBuilder();
		builder.append("{\"id\":\"");
		builder.append(id);
		builder.append("\", \"");
		if (name != null)
		{
			builder.append("name\":\"");
			builder.append(name);
		}
		builder.append("\"}");
		return builder.toString();
	}
}
